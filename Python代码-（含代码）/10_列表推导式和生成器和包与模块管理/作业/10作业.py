year = int(input('请输入年：'))
month = int(input('请输入月份：'))

def is_leap_year(year):
    return year % 4 == 0 and year % 100 != 0 or year % 400 == 0

def month_days(month,year):
    if month % 2 != 0:
        return 31
    elif is_leap_year(year) and month == 2:
        return 29
    elif month == 2:
        return 28
    else:
        return 30


